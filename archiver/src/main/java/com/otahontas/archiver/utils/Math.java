package com.otahontas.archiver.utils;

/**
 * Custom Math functions library
 * */

public class Math {

    /**
     * Returns the absolute value of double given.
     *
     * @param value Original value
     * @return Absolute value of given original value
     */
    public static double abs(double value) {
        return value >= 0 ? value : -value;
    }

}
